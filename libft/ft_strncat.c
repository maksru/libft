#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	h;

	h = ft_strlen(s1);
	i = 0;
	while (i < n && s2[i] != '\0')
	{
		s1[h] = s2[i];
		i++;
		h++;
	}
	s1[h] = '\0';
	return (s1);
}
