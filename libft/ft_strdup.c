#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(char *str)
{
	char	*dest;
	int		i;

	if ((dest = (char *)malloc(ft_strlen(str) + 1)) == NULL)
		return (0);
	i = 0;
	while (str[i])
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
